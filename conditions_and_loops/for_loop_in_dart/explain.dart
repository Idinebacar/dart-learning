/*
  Example: For loop in Dart

  - For loop is used when we want to repeat a block of code 
    a specific number of times.
*/

void main() {
  // Initialization: int i = 1 (runs once at the beginning)
  // Condition: i <= 5 (checked before each iteration)
  // Increment: i++ (updates after each iteration)
  for (int i = 1; i <= 5; i++) {
    print("Iteration number: $i");
  }
}
